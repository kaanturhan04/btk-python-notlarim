# Değişkenler - BTK Python Eğitimi Notları

# Sayısal değişkenler
sayi1 = 10
sayi2 = 20
toplam = sayi1 + sayi2
print("Toplam:", toplam)

# Metinsel değişkenler
ad = "Kaan"
soyad = "Turhan"
tam_ad = ad + " " + soyad
print("Ad Soyad:", tam_ad)

# Boolean (Mantıksal) değişken
ogrenci_mi = True
print("Öğrenci misin?:", ogrenci_mi)
